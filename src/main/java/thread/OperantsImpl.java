package thread;
public class OperantsImpl implements Operations {

    public int sum(int x, int y) {
        return x+y;
    }

    public int sub(int x, int y) {
        return x-y;
    }

    public int multiplication(int x, int y) {
        return x*y;
    }

    public int division(int x, int y) {
        return x/y;
    }
}
